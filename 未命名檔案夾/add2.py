import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    #######################
    #                     #
    #        TODO:        #
    #                     #
    #######################
    lst1 = []
    lst2 = []
    cur = l1
    ans = 0
    while cur is not None:
        lst1.append(cur.val)
        cur = cur.next
    cur = l2
    while cur is not None:
        lst2.append(cur.val)
        cur = cur.next
    for i in range(len(lst1) - 1, -1, -1):
        ans += lst1[i] * (10 ** (len(lst1) - 1 - i))
    for j in range(len(lst2) - 1, -1, -1):
        ans += lst2[j] * (10 ** (len(lst2) - 1 - j))
    ans = str(ans)
    ll = len(ans)
    li = ListNode(int(ans[0]))
    for k in range(1, ll):
        f = ListNode(int(ans[k]))
        f.next = li
        li = f
    return li


